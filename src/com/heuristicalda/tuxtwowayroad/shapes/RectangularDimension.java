package com.heuristicalda.tuxtwowayroad.shapes;

public class RectangularDimension {
	private int height ;
	private int width;
	
	public RectangularDimension(int height,int width)
	{
		this.height = height;
		this.width = width;
	}
	
	public int getHeight()
	{
		return this.height;
	}
	
	public int getWidth()
	{
		return this.width;
	}
}
